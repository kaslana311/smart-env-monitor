/*
 * app_config.h
 *
 * 智能环境监测系统 - 全局配置与类型定义
 * 本文件为所有模块提供共享的数据类型、线程参数和IPC对象声明。
 *
 * 归属: 共享文件（成员A负责维护）
 */

#ifndef APP_CONFIG_H__
#define APP_CONFIG_H__

#include <rtthread.h>

/* ================================================================
 * 项目信息
 * ================================================================ */
#define APP_NAME        "Smart Environment Monitor"
#define APP_VERSION     "v1.0.0"
#define APP_AUTHOR      "Group 3"
#define APP_DATE        "2026-07-10"

/* ================================================================
 * 线程优先级定义 (数值越小优先级越高)
 * ================================================================ */
#define SENSOR_THREAD_PRIORITY   15   /* 传感器采集线程 */
#define PROCESS_THREAD_PRIORITY  16   /* 数据处理线程 */
#define DISPLAY_THREAD_PRIORITY  17   /* 显示输出线程 */
#define ALARM_THREAD_PRIORITY    18   /* 告警监控线程 */

/* ================================================================
 * 线程栈大小定义 (单位: 字节)
 * ================================================================ */
#define SENSOR_THREAD_STACK_SIZE   1024
#define PROCESS_THREAD_STACK_SIZE  1024
#define DISPLAY_THREAD_STACK_SIZE  1024
#define ALARM_THREAD_STACK_SIZE    512

/* ================================================================
 * 线程时间片定义
 * ================================================================ */
#define SENSOR_THREAD_TICK    (RT_TICK_PER_SECOND / 2)   /* 500ms 采集周期 */
#define DISPLAY_THREAD_TICK   (RT_TICK_PER_SECOND)       /* 1s 显示刷新 */
#define ALARM_THREAD_TICK     (RT_TICK_PER_SECOND / 5)   /* 200ms 告警检查 */

/* ================================================================
 * 数据结构定义
 * ================================================================ */

/* 传感器原始数据 */
typedef struct {
    float temperature;      /* 温度 (°C), 范围 -10.0 ~ 45.0 */
    float humidity;         /* 湿度 (%),  范围 20.0 ~ 95.0 */
    float light;            /* 光照 (lux), 范围 0 ~ 1000 */
} sensor_data_t;

/* ================================================================
 * 滤波器参数
 * ================================================================ */
#define FILTER_WINDOW_SIZE  5       /* 移动平均滤波窗口大小 */

/* ================================================================
 * 阈值定义 (用于告警判断)
 * ================================================================ */
#define TEMP_HIGH_THRESHOLD      35.0f   /* 高温告警阈值 (°C) */
#define HUMIDITY_HIGH_THRESHOLD  90.0f   /* 高湿告警阈值 (%)  */
#define LIGHT_LOW_THRESHOLD      50.0f   /* 低光照阈值 (lux)  */

/* ================================================================
 * 系统状态标志位
 * ================================================================ */
#define SYS_FLAG_NORMAL      0x00        /* 正常状态 */
#define SYS_FLAG_TEMP_HIGH   (1 << 0)   /* 位0: 温度过高 */
#define SYS_FLAG_HUMID_HIGH  (1 << 1)   /* 位1: 湿度过高 */
#define SYS_FLAG_LIGHT_LOW   (1 << 2)   /* 位2: 光照过低 */

/* ================================================================
 * 系统全局状态 (互斥量保护)
 * ================================================================ */
typedef struct {
    uint8_t     alarm_flags;        /* 告警标志位组合 */
    sensor_data_t latest_data;      /* 最新处理后的传感器数据 */
    uint32_t    sample_count;       /* 采样计数 */
} system_status_t;

/* ================================================================
 * 显示模块配置
 * ================================================================ */
typedef enum {
    DISP_MODE_BRIEF,     /* 简洁模式: 单行输出，适合串口调试 */
    DISP_MODE_DETAIL     /* 详细模式: 完整格式化显示 */
} display_mode_t;

#define DEFAULT_DISPLAY_MODE  DISP_MODE_DETAIL   /* 默认显示模式 */
#define DISPLAY_LINE_WIDTH    40                  /* 显示分隔线宽度 */

/* ================================================================
 * LED 引脚定义 (根据实际硬件修改)
 * STM32F407IG: 使用 GPIO 引脚
 * ================================================================ */
#define LED0_PIN    GET_PIN(F, 9)   /* 心跳指示灯 */
#define LED1_PIN    GET_PIN(F, 10)  /* 告警指示灯 */

/* ================================================================
 * 消息队列参数
 * ================================================================ */
#define MQ_SENSOR_MAX_MSGS  10      /* 消息队列最大消息数 */

/* ================================================================
 * IPC 对象外部声明 (定义在 main.c 中)
 * ================================================================ */
extern struct rt_messagequeue mq_sensor_data;   /* 传感器→处理 消息队列 */
extern struct rt_mutex        mutex_sys_status;  /* 系统状态互斥量 */
extern struct rt_semaphore    sem_display;       /* 数据就绪信号量 */

/* ================================================================
 * 模块初始化函数声明
 * ================================================================ */
int sensor_init(void);
int process_init(void);
int display_init(void);
int alarm_init(void);

#endif /* APP_CONFIG_H__ */
